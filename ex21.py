def add(a,b):
    print(f"{a}+{b}")
    return a+b

def subtract(a,b):
    print(f"{a}-{b}")
    return a-b

def multiply(a,b):
    print(f"{a}*{b}")
    return a*b

def divide(a,b):
    print(f"{a}/{b}")
    return a/b

an1=add(1,2)
an2=subtract(3,4)
an3=multiply(5,6)
an4=divide(7,8)

print(f"{an1},{an2},{an3},{an4}")
